func (this *app) getStatus(w http.ResponseWriter, r *http.Request) {
	t, err := template.ParseFiles(opts.Templates + "templates/status.html")
	if err != nil {
		log.Fatal(err)
	}

	err = t.Execute(w, nil)
	if err != nil {
        log.Print(err.Error())
        http.Error(w, "Internal Server Error", http.StatusInternalServerError)
    }
}
